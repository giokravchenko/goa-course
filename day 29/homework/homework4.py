#4)შენი სიტყვებით ახსენი რას ნიშნავს decrementation და incrementation ასევე მოიყვანე მაგალითები აუცილებლად

#decrementation = ეს ნიშნავს შემცირებას მაგალითად როცა while ს დახმარებით რიცხვს ამცირებ ეგ არის decrementation-შემცირება ს
#incrementation = ეს ნიშნავს ზრდას მაგალითად როცა while ს დახმარებით ზრდი რიცხვს მას ეწოდება incrementation-ზრდა ს